Casting: 
	- Analogy: metal casting(melt the metal, put into different shapes).
- Converting one variable into another variable format.
- Casting can eb done for only singular varibles.
	- int a; //singular variable
	- int b[4:0]; //not singular
	
- Variables can be objects, non-objects 
	- Non-objects: Data type is known at the time of compile. Hence, this type of casting is called static casting.
	- Object: Data type is decided at the un time.
		- Object can be of any class type(it cab be base class, derived class)
		- Hence called dynamic casting.